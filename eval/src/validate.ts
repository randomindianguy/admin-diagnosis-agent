import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import Anthropic from "@anthropic-ai/sdk";
import dotenv from "dotenv";
import type { AgentOutput, GradeFields, GradeResult, Scenario } from "./types";
import { gradeOutput } from "./grade";
import { JUDGE_MODEL } from "./judges";

dotenv.config();

const here = dirname(fileURLToPath(import.meta.url));
const chunkRoot = resolve(here, "../.."); // Project/

function loadJson<T>(relPath: string): T {
  return JSON.parse(readFileSync(resolve(chunkRoot, relPath), "utf8")) as T;
}

function fmt(b: boolean): string {
  return b ? "PASS" : "FAIL";
}

// Q4 — per-field shape-check. The done-criterion is these exact shapes, not a
// single overall pass/fail bit. `wrong` must fail for the RIGHT reasons.
const EXPECTED_SHAPE: Record<string, GradeFields> = {
  good: { verdict: true, root_cause: true, criterion_1: true, criterion_2: true },
  wrong: { verdict: true, root_cause: false, criterion_1: false, criterion_2: false },
};

function shapeMatches(f: GradeFields, e: GradeFields): boolean {
  return (
    f.verdict === e.verdict &&
    f.root_cause === e.root_cause &&
    f.criterion_1 === e.criterion_1 &&
    f.criterion_2 === e.criterion_2
  );
}

async function main(): Promise<void> {
  if (!process.env.ANTHROPIC_API_KEY) {
    console.error(
      "ANTHROPIC_API_KEY is not set. Copy .env.example to .env and add your key (see README).",
    );
    process.exit(1);
  }

  const scenario = loadJson<Scenario>("scenario.json");
  const good = loadJson<AgentOutput>("agent-outputs/good.json");
  const wrong = loadJson<AgentOutput>("agent-outputs/wrong.json");

  const client = new Anthropic();

  const results: Record<string, GradeResult> = {
    good: await gradeOutput(client, scenario, good),
    wrong: await gradeOutput(client, scenario, wrong),
  };

  const order = ["good", "wrong"] as const;

  for (const key of order) {
    const f = results[key].fields;
    const matches = shapeMatches(f, EXPECTED_SHAPE[key]);
    const tag = !matches
      ? "RULER BROKEN ✗"
      : key === "good"
        ? "RULER PASSES ✓"
        : "RULER FAILS ✗ (correct)";
    console.log(
      `[${key.padEnd(5)}] verdict=${fmt(f.verdict)}  root_cause=${fmt(f.root_cause)}  ` +
        `criterion_1=${fmt(f.criterion_1)}  criterion_2=${fmt(f.criterion_2)}  →  ${tag}`,
    );
  }

  const satisfied = order.every((k) => shapeMatches(results[k].fields, EXPECTED_SHAPE[k]));

  console.log("");
  if (satisfied) {
    console.log("Done-criterion: SATISFIED ✓");
    writeValidationNotes(results);
    process.exit(0);
  }
  console.log(
    "Done-criterion: NOT SATISFIED ✗ — the ruler is broken. Fix the ruler, not the outputs or the scenario.",
  );
  process.exit(1);
}

// The marker that separates code-owned facts (§1–2) from hand-authored judgment
// (§3–4). Everything from this token onward is preserved verbatim across re-runs.
const NOTES_PRESERVE_BOUNDARY = "## 3.";

// Q1/Q8 — code owns the facts: §1–2 are regenerated from the structured GradeResults.
// §3–4 are hand-authored (judgment) and PRESERVED across re-runs — when the notes
// file already exists, only the §1–2 region is rewritten; everything from the
// boundary onward is carried over verbatim so a re-run never clobbers prose.
function writeValidationNotes(results: Record<string, GradeResult>): void {
  const notesPath = resolve(chunkRoot, "VALIDATION-NOTES.md");

  const row = (key: string): string => {
    const f = results[key].fields;
    return `| ${key} | ${fmt(f.verdict)} | ${fmt(f.root_cause)} | ${fmt(f.criterion_1)} | ${fmt(f.criterion_2)} |`;
  };
  const reason = (
    key: string,
    crit: "criterion_1" | "criterion_2",
    label: string,
  ): string => {
    const r = results[key].judgeRaw[crit];
    return `- **${key} · ${crit} (${label}) → ${r.answer}:** ${r.reasoning}`;
  };

  const facts = `# Validation Notes — Chunk 1 (Seed 1)

> §1–2 are generated by the validation run from the structured grade results (facts — no transcription).
> §3–4 are hand-authored (judgment) and preserved across re-runs.

## 1. What was tested
- **Scenario:** \`${results.good.scenarioId}\` — Seed 1, nested-subgroup inheritance gap, confident-resolve.
- **Outputs graded:** \`good\` (handcrafted to meet all four dimensions) and \`wrong\` (handcrafted to fail root_cause + both judges; verdict passes by design).
- **Fields per output:** \`verdict\` and \`root_cause\` (code exact-match), \`criterion_1\` = mechanism_identification and \`criterion_2\` = operator_reconciliation (binary LLM-judge, flat AND).
- **Judge:** \`${JUDGE_MODEL}\`, one call per criterion, strict yes/no parse on the first line; reasoning captured as a supplementary audit column (Q7), not graded.

## 2. What the ruler reported

| output | verdict | root_cause | criterion_1 | criterion_2 |
|---|---|---|---|---|
${row("good")}
${row("wrong")}

### Judge reasoning (supplementary — captured, not graded)
${reason("good", "criterion_1", "mechanism")}
${reason("good", "criterion_2", "reconciliation")}
${reason("wrong", "criterion_1", "mechanism")}
${reason("wrong", "criterion_2", "reconciliation")}

`;

  const stub = `## 3. Why each report is correct
<!-- HAND-AUTHORED after the run. Tie each PASS/FAIL to the underlying mechanism, not "they matched". -->

## 4. What would invalidate this ruler going forward
<!-- HAND-AUTHORED after the run. Conditions under which today's PASS reading would stop being reliable. -->
`;

  let tail = stub;
  if (existsSync(notesPath)) {
    const existing = readFileSync(notesPath, "utf8");
    const idx = existing.indexOf(NOTES_PRESERVE_BOUNDARY);
    // Fail loud (Q3 discipline) — never half-write. If the boundary is gone, the
    // hand-authored §3–4 can't be located, so refuse rather than silently stub them.
    if (idx < 0) {
      throw new Error(
        `VALIDATION-NOTES.md exists but contains no '${NOTES_PRESERVE_BOUNDARY}' boundary marker — ` +
          `cannot locate the hand-authored §3–4 to preserve. Refusing to overwrite. Inspect the file before re-running.`,
      );
    }
    tail = existing.slice(idx);
  }

  writeFileSync(notesPath, facts + tail, "utf8");
}

main().catch((err: unknown) => {
  console.error(err instanceof Error ? err.message : err);
  process.exit(1);
});
